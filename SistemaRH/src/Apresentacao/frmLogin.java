    private void btnLogin()
    {
        //CODIGO DO BOT√ÉO LOGIN
        
        LoginUsuarioControle controle = new LoginUsuarioControle(txtLogin.getText(), txtSenha.getText());
        lblMensagem.setText(controle.getMensagem()); 

        if(controle.getMensagem().equals("Login efetuado com sucesso."))
        {
            try
            {
                frmPrincipal frmP = new frmPrincipal(txtLogin.getText());
                this.dispose();
                frmP.setVisible(true);
            }
            catch(Exception e)
            {

            }
        }
        else
        {
            txtLogin.requestFocus();
            txtSenha.setText("");
        }
    }
    
